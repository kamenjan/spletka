    <h1>O ŠILI</h2>
        <h3>*ŠILA – Šolska impro liga*</h3>
        <p> tekmovanje srednješolskih skupin v gledališkh
            improvizacijah,  je projekt, ki že od leta 1997 spodbuja kulturno
            rekreacijo mladih. V okviru projekta mlade seznanjamo z osnovami odrske
            umetnosti – nudimo številne delavnice ter izobraževanja z domačimi in
            tujimi mentorji, obenem pa možnost, da pridobljeno znanje in veščine
            pokažejo tudi z nastopom na gledališkem odru.

            Gre za kombinacijo improvizirane gledališke predstave in tekmovanja, kjer
            imajo gledalci glavno besedo pri tem, kaj improvizatorji na odru počnejo.
            Sodelujejo ekipe iz različnih srednjih šol, ki so locirane po vseh koncih
            Slovenije, na koncu sezone pa zmaga le ena.

            Pri projektu vsako leto sodeluje okoli 350 srednješolk in srednješolcev,
            cilj ŠILE pa je ponuditi mladini sproščeno in zanimivo dejavnost, s katero
            bodo ustvarjalno preživljali prosti čas.</p>

        <h3>*ŠILA valilnica *</h3>
        <p>je projekt namenjen srednješolcem, ki se prvo leto
            spoznavajo z gledališko improvizacijo. Na celoletnih delavnicah se
            učijo komunicirati med seboj in okoljem, artikulirali svoje misli in
            želje, prevzemati odgovornost za svoja dejanja, izražati svoja
            čustva, sodelovati v skupini in se poslušati, spoznavati izrazno plat
            jezika in jo zavestno kultivirati pri govornem in pisnem sporočanju, se
            vračati k spontanosti, kjer se sproščata osebna svoboda in
            ustvarjalnost, sprejemati drugačnosti vseh vrst ter se osvoboditi
            predvidljivih reakcij in naučenih odgovorov. Ob koncu sezone se predstavijo
            še pred občinstvom in tako vadijo sproščeno javno nastopanje in
            premagovanje treme.</p>

        <h3>*MIŠ - Mala impro šola*</h3>
        <p> je projekt, ki že osnovnošolcem nudi možnost
            spoznavanja z gledališko improvizacijo na popolnoma netekmovalen način. Na
            delavnicah z izkušenimi mentorji učenci spoznajo svet improvizacije, poleg
            pa imajo priložnost svoje znanje pokazati tudi na odru v okviru predstav
            MIŠ.</p>

