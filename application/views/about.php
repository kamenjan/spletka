	    <h1>O ŠILI</h2>
        <h3>ŠILA – Šolska impro liga, tekmovanje srednješolskih skupin v gledališkh improvizacijah</h3>
        <p> je projekt, ki spodbuja kulturno rekreacijo mladih. Od leta 1997 do leta 2013 smo ga izvajali v KUD-u France Prešeren, z letošnjim letom pa smo ga začeli izvajati v okviru Društva za mladinsko kulturo in izobraževanje Kdo? Kje? Kaj?, prireditve pa izvajamo v glavnem v Centru kulture Španski borci.
            V okviru projekta mlade seznanjamo z osnovami odrske umetnosti – nudimo jim številne delavnice in izobraževanja z domačimi in tujimi mentorji, obenem pa možnost, da pridobljeno znanje in veščine pokažejo tudi z nastopom na gledališkem odru. Ob tem spodbujamo njihove ustvarjalnost, družbeno angažiranost in socialno odgovorno delovanje. Na delavnicah se poleg učenja improvizacijskih veščin učijo tudi socialnih – solidarnost do drugih, pomembnost enakovrednosti med posamezniki, spodbujanje k nenasilju in sprejemanju drugačnosti ter preprečevanje diskriminacije.  Tako krepijo samozavest in sposobnost javnega nastopanja, sodelovanja in timskega dela, razvijajo kreativno mišljenje in še mnogo več.
            Vsako leto sodeluje okoli 350 srednješolk in srednješolcev.
            Cilj ŠILE je ponuditi mladini sproščeno in zanimivo dejavnost, s katero bodo ustvarjalno preživljali prosti čas.</p>

